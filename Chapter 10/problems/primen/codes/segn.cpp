void getp(int n=maxn)
{
    memset(prm,0,sizeof(prm));
	for(int i=2;i<=n;i++){
	    if(!prm[i])prm[++prm[0]]=i;
	    for(int j=1;j<=prm[0] && prm[j]<=n/i;j++){
	        prm[prm[j]*i]=1;
	        if(i%prm[j]==0)break;
        }
	}
}

int segn(int nn)
{
	double a=log(double(nn)),b=log(a);
	int v=nn*(a+b-1),n=nn*(a+b-0.9484);
    int m=sqrt(n),k,i,j,id,s,p,r;
    getp(max(m,maxn));
    if(nn<=prm[0])return prm[nn];
    int kk=min((int)100,v/sz),ret=0;
    if(kk)ret=pcn[kk-1];
    for(k=kk;k*sz<=n;k++){
        memset(seg,1,sizeof(seg));
        for(i=1,s=k*sz;i<=prm[0];i++){
        	p=prm[i];id=(s+p-1)/p;
            for(j=max(id,p)*p-s;j<sz;j+=p)seg[j]=0;
        }
        if(k==0)seg[0]=seg[1]=0;
        for(i=0;i<sz && s+i<=n;i++){
        	if(seg[i]){
        		ret++;
        		if(ret==nn)return k*sz+i;
			}
		}
    }
    return 0;
}

